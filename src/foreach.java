public class foreach {
    public static void main (String[] args)
    {
        String[] tab = {"Przemek", "Daria", "Bartek", "Krystian"};
/// item zmienna tymczasowa
/// iteruje po wszystkich item - ach tablicy

        for(String item : tab)
        {
            System.out.println(item);

        }


    }
}
